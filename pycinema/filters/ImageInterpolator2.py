class ImageInterpolator2(Filter):
    def __init__(self):
        self.cache = {}
        super().__init__(
            inputs={
                'images': [],
                'nFrames': 1
            },
            outputs={
                'images': []
            }
        )

    def inference_adaptive(self, model, channel1, channel2, nFrames):
        # preprocess channels
        img0 = (torch.tensor(channel1.transpose(2, 0, 1)).to(device) / 255.).unsqueeze(0)
        img1 = (torch.tensor(channel2.transpose(2, 0, 1)).to(device) / 255.).unsqueeze(0)
        img0 = img0[:, :3, :, :]
        img1 = img1[:, :3, :, :]
        n, c, h, w = img0.shape
        ph = ((h - 1) // 32 + 1) * 32
        pw = ((w - 1) // 32 + 1) * 32
        padding = (0, pw - w, 0, ph - h)
        img0 = F.pad(img0, padding)
        img1 = F.pad(img1, padding)

        img_list = [img0, img1]
        for i in range(nFrames):
            tmp = []
            for j in range(len(img_list) - 1):
                mid = model.inference(img_list[j], img_list[j + 1])
                tmp.append(img_list[j])
                tmp.append(mid)
            tmp.append(img1)
            img_list = tmp

        result = []
        for i in range(1,len(img_list)-1):
          temp = img_list[i].squeeze(0).cpu().detach().numpy()
          alpha_channel = np.ones((temp.shape[1], temp.shape[2]), dtype=temp.dtype)
          temp_rgba = np.dstack((temp.transpose(1, 2, 0), alpha_channel))
          temp_rgba = (temp_rgba * 255).astype(np.uint8)
          result.append(np.ascontiguousarray(temp_rgba))

        return result

    def _update(self):
        images = self.inputs.images.get()
        nFrames = self.inputs.nFrames.get()

        model = Model()
        model.load_model(-1)

        # generate interpolated images
        output_images = []
        for i in range(0, len(images) - 1):
            rgba1 = images[i].channels['rgba']
            rgba2 = images[i + 1].channels['rgba']

            rgbaI = self.inference_adaptive(model, rgba1, rgba2, nFrames)

            # Add the first image (starting image)
            output_images.append(Image(
                {'rgba': rgba1},
                interpolate_meta(images[i].meta, images[i + 1].meta, 0)
            ))

            # Add interpolated images
            for l in range(0, len(rgbaI)):
                output_images.append(Image(
                    {'rgba': rgbaI[l]},
                    interpolate_meta(images[i].meta, images[i + 1].meta, (l + 1) / (nFrames + 1))
                ))

        # Add the last image (ending image)
        output_images.append(Image(
            {'rgba': images[-1].channels['rgba']},
            interpolate_meta(images[-1].meta, images[-1].meta, 1)
        ))

        self.outputs.images.set(output_images)

        return 1
